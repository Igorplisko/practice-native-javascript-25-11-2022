//! === JavaScript Comparison and Logical Operators
//  ===== operatory  sravneniya i  usloviya   ======

let x = 5; //!

//*  ==	 equal to

let x1 = x == 8; //returns false
let x2 = x == 5; //returns true
let x3 = x == '5'; //returns true

//* ===	equal value and equal type
let x4 = x === 5; //returns true
let x5 = x === '5'; //returns false

//* !=	not equal
let x6 = x != 8; //returns true

//* !==	not equal value or not equal type
let x7 = x !== 5; //returns false
let x8 = x !== '5'; //returns	true
let x9 = x !== 8; //returns true

//* >	greater than
let x10 = x > 8; //returns false

//* <	less than
let x11 = x < 8; //returns true

//* >=	greater than or equal to
let x12 = x >= 8; //returns false

//* <=	less than or equal to
let x13 = x <= 8; //returns true
